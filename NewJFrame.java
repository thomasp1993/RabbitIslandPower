    private void btnSearchNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNumberActionPerformed
        try{
            int pos = rs.getRow();
            rs.beforeFirst();
            int count=0;
            while (rs.next()){
                count++;
            }
            rs.first();
            String str=JOptionPane.showInputDialog(null,"Εισαγωγή Αριθμού Κάρτας" );
            int n= Integer.parseInt(str);
            
            if(n<=count){
                rs.absolute(n);
                set();
                TextCurrent.setText(Integer.toString(rs.getRow()));
            }
            else{
                JOptionPane.showMessageDialog(null, "Δεν υπάρχει ο αριθμός");
                rs.absolute(pos);
            }
        }
        catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_btnSearchNumberActionPerformed

    private void btnAddPointsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPointsActionPerformed
        try{
            int AddPoints = Integer.parseInt(JOptionPane.showInputDialog(null, "Πόσοι πόντοι να προσθεθούν;"));
        
            int Points=Integer.parseInt(rs.getString(6));
            Points+=AddPoints;
            
            if(Points>=800){
                JOptionPane.showMessageDialog(null,"O πελάτης "+ rs.getString(2)+" κέρδισε 2 BONUS!","BONUS",JOptionPane.INFORMATION_MESSAGE);
                Points-=800;
                //ΑΥΞΗΣΕ ΤΟ ΒΟΝUS ΚΑΤΑ 2
                int bon=Integer.parseInt(textBonus.getText());
                bon+=2;
                rs.updateInt(7, bon);
            }
            
            else if(Points>=400){
                JOptionPane.showMessageDialog(null,"O πελάτης "+ rs.getString(2)+" κέρδισε BONUS!","BONUS",JOptionPane.INFORMATION_MESSAGE);
                Points-=400;
                //ΑΥΞΗΣΕ ΤΟ ΒΟΝUS ΚΑΤΑ 1
                int bon=Integer.parseInt(textBonus.getText());
                bon++;
                rs.updateInt(7, bon);
            }
            
            rs.updateInt(6, Points);
            set();
        }
        catch(SQLException err){
            JOptionPane.showMessageDialog(null,err.getMessage());
        }
    }//GEN-LAST:event_btnAddPointsActionPerformed

