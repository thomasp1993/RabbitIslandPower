    public void doConnect(){
        
        try{
            
            //CONNECT TO DATABASE
            con = DriverManager.getConnection("jdbc:derby:vet_database1;create=true;");
           
            //CHECK IF TABLE ALREADY EXISTS AND ADD A DEFAULT ROW IF IT DOESN'T
            STMT =con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            dbm = con.getMetaData();
            ResultSet tables = dbm.getTables(null, null, "PELATES", null);
            if(!tables.next()){
                String createTable="CREATE TABLE PELATES (arithmos INT, onomatepwnumo VARCHAR(100),dieuthinsi VARCHAR(100), stathero BIGINT, kinito BIGINT, pontoi INT, xbonus INT, xreos DOUBLE)";
                String insertRow = "INSERT INTO PELATES VALUES (1, 'ΟΝΟΜΑ', 'ΔΙΕΥΘΥΝΣΗ', 0, 0, 0, 0, 0)";
                STMT.executeUpdate(createTable);
                STMT.executeUpdate(insertRow);
                STMT.close();
            }
            
            Reload();
          
        }
        catch(SQLException err){
            
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
    public void Reload(){
        try{
            
            //EXECUTE SQL, LOAD INTO THE RESULTSET OBJECT
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = stmt.executeQuery("SELECT * FROM PELATES");
            
            //MOVE CURSOR TO FIRST ROW, ADD ALL THE DATA
            rs.next();
            set();
        }
        catch(SQLException err){
            
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
        
    }
    
    public void set(){
        try{
            
            textCard.setText(Integer.toString(rs.getInt(1)));
            textName.setText(rs.getString(2));
            textAddress.setText(rs.getString(3));
            textHome.setText(Long.toString(rs.getLong(4)));
            textMobile.setText(Long.toString(rs.getLong(5)));
            textPoints.setText(Integer.toString(rs.getInt(6)));
            textBonus.setText(Integer.toString(rs.getInt(7)));
            textDebt.setText(Double.toString(rs.getDouble(8)));
            
            
            int pos = rs.getRow();
            rs.beforeFirst();
            int total=0;
            while(rs.next()){
                total++;
            }
            rs.absolute(pos);
            TextAll.setText(Integer.toString(total));
                        
        }
        catch(SQLException err){
            JOptionPane.showMessageDialog(NewJFrame.this, err.getMessage());
        }
    }
    
  

